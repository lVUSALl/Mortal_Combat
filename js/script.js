function play1() {
    var audio = new Audio( 'mp3/goro-wins.mp3');
    audio.play();
    document.getElementsByClassName('first-side')[0].style.display = 'none';
    document.getElementById('fighters').style.display = 'none'; 
    var mainimage = document.createElement('img');
    mainimage.src =  'img/goro-main.jpg'
    document.body.appendChild(mainimage)
    document.getElementsByTagName('body')[0].style.display = 'flex'
    mainimage.style.height = '750px'
    document.getElementsByTagName('body')[0].style.justifyContent = 'center'
}

